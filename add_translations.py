def main():
    with open('data/yijing_64_gua.json', 'r', encoding='utf-8') as f:
        data = json.load(f)

    errors = []
    for gua in data['hexagrams']:
        gid = gua['id']
        if gid not in TRANSLATIONS:
            errors.append(f"Missing translation for hexagram {gid} ({gua['name']})")
            continue
        t = TRANSLATIONS[gid]
        gua['gua_ci_translation'] = t[0]
        gua['xiang_ci_translation'] = t[1]
        if len(t[2]) != 6:
            errors.append(f"Hexagram {gid}: expected 6 translations, got {len(t[2])}")
            continue
        for i, yao in enumerate(gua['yaoci']):
            yao['translation'] = t[2][i]

    if errors:
        print("ERRORS:")
        for e in errors:
            print(f"  {e}")
        sys.exit(1)

    # Verify
    total_yao_trans = sum(len(g['yaoci']) for g in data['hexagrams'])
    trans_count = sum(1 for g in data['hexagrams'] for y in g['yaoci'] if 'translation' in y)
    print(f"Updated {len(data['hexagrams'])} hexagrams")
    print(f"Yao translations: {trans_count}/{total_yao_trans}")
    print(f"Gua ci translations: {sum(1 for g in data['hexagrams'] if 'gua_ci_translation' in g)}/64")
    print(f"Xiang ci translations: {sum(1 for g in data['hexagrams'] if 'xiang_ci_translation' in g)}/64")

    with open('data/yijing_64_gua.json', 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print("Done! Saved to data/yijing_64_gua.json")

if __name__ == '__main__':
    main()
