def update_translations():
    with open('C:/Users/lky/WorkBuddy/Claw/data/yijing_64_gua.json', 'r', encoding='utf-8') as f:
        data = json.load(f)

    updated = 0
    skipped = 0
    for h in data['hexagrams']:
        tid = h['id']
        if tid in TRANSLATIONS:
            t = TRANSLATIONS[tid]
            h['gua_ci_translation'] = t['gc']
            h['xiang_ci_translation'] = t['xc']
            for i, yt in enumerate(t['y']):
                h['yaoci'][i]['translation'] = yt
            updated += 1
        else:
            print(f"WARNING: No translation for hexagram {tid} {h['name']}")
            skipped += 1

    print(f"Updated: {updated}/64 hexagrams")
    print(f"Skipped: {skipped}")

    # Verify
    good = 0
    total = 0
    for h in data['hexagrams']:
        for y in h['yaoci']:
            total += 1
            t = y.get('translation', '')
            if t and t != y['text'] and len(t) > 5:
                good += 1
    print(f"Valid yaoci translations: {good}/{total}")

    with open('C:/Users/lky/WorkBuddy/Claw/data/yijing_64_gua.json', 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    print("JSON saved.")

