def validate():
    errors = []
    if len(HEXAGRAMS) != 64:
        errors.append(f"hexagram count: {len(HEXAGRAMS)}, expected 64")
    ids = set()
    for h in HEXAGRAMS:
        ids.add(h["id"])
        if len(h["yaoci"]) != 6:
            errors.append(f"hexagram {h['id']} ({h['name']}): yaoci count={len(h['yaoci'])}, expected 6")
        for i, y in enumerate(h["yaoci"]):
            if y["pos"] != i + 1:
                errors.append(f"hexagram {h['id']} yaoci pos error at index {i}")
        if h["upper"] not in BAGUA or h["lower"] not in BAGUA:
            errors.append(f"hexagram {h['id']} invalid trigram ref")
        if not h.get("gua_ci") or not h.get("xiang_ci"):
            errors.append(f"hexagram {h['id']} missing gua_ci or xiang_ci")
    if len(ids) != 64:
        errors.append(f"duplicate hexagram ids: {64 - len(ids)} dupes")
    return errors

